    init {
        title = "Create Pull Request"
        setOKButtonText("Create PR")
        init()

        // Register the chip widget for disposal so its timer-backed icons are released.
        Disposer.register(disposable, ticketChipInput)

        // AI title extension (gated by settings).
        if (aiTitleEnabled && titleField is ExtendableTextField) {
            val icon = IconLoader.getIcon("/icons/ai-sparkle.svg", CreatePrDialog::class.java)
            val ext = ExtendableTextComponent.Extension.create(
                icon, icon, "Generate title with AI"
            ) { onAiTitleClicked() }
            aiTitleExtension = ext
            (titleField as ExtendableTextField).addExtension(ext)
        }

        // Target branch dropdown chevron — opens a searchable JBPopup list (filterable by
        // typing, click/Enter to select, Esc to close). IntelliJ's standard pattern; replaces
        // the previous JPopupMenu-based autocomplete which had several UX problems: the
        // popup had no filter field, item clicks sometimes failed to propagate when opened
        // from an ExtendableTextField icon, and the DocumentListener kept re-spawning the
        // popup while the user was trying to dismiss it.
        val chevron = com.intellij.icons.AllIcons.General.ArrowDown
        targetField.addExtension(
            ExtendableTextComponent.Extension.create(chevron, chevron, "Show branches") {
                showBranchChooser()
            }
        )

        // Pre-fill reviewers: union of settings-configured defaults AND the selected repo's
        // Bitbucket-configured default reviewers (from the default-reviewers plugin, fetched
        // during prefetch). Track which usernames came from the repo-level list so we can
        // swap them out if the user changes modules without clobbering manually-added chips.
        val initialReviewers = (context.defaultReviewers + currentRepo.repoDefaultReviewers).distinct()
        initialReviewers.forEach { addReviewerChip(it, fromRepoDefaults = it in currentRepo.repoDefaultReviewers) }

        // Populate transitions
        if (context.transitions.isNotEmpty() && context.initialTicketKeys.isNotEmpty()) {
            context.transitions.forEach { transitionCombo.addItem(TransitionItem(it)) }
            val reviewIdx = context.transitions.indexOfFirst {
                it.name.contains("Review", ignoreCase = true) ||
                    it.name.contains("In Review", ignoreCase = true)
            }
            if (reviewIdx >= 0) transitionCombo.selectedIndex = reviewIdx
        } else {
            transitionCheckbox.isVisible = false
            transitionCombo.isVisible = false
        }

        // Wire module combo selection change
        moduleCombo?.addActionListener {
            val selected = moduleCombo.selectedItem as? RepoComboItem ?: return@addActionListener
            if (selected.repoPrefetch != currentRepo) {
                onModuleChanged(selected.repoPrefetch)
            }
        }

        // Wire provenance invalidation only — target-branch autocomplete-while-typing was
        // removed along with the old JPopupMenu approach. Users now discover branches via
        // the chevron dropdown; free text in the field remains valid (git push/PR endpoint
        // validates it server-side).
        targetField.document.addDocumentListener(object : DocumentListener {
            override fun insertUpdate(e: DocumentEvent) = onTargetChanged()
            override fun removeUpdate(e: DocumentEvent) = onTargetChanged()
            override fun changedUpdate(e: DocumentEvent) = onTargetChanged()
        })

        // Wire reviewer search
        reviewerInput.document.addDocumentListener(object : DocumentListener {
            override fun insertUpdate(e: DocumentEvent) = searchUsers()
            override fun removeUpdate(e: DocumentEvent) = searchUsers()
            override fun changedUpdate(e: DocumentEvent) = searchUsers()
        })

        // Wire description tabs
        editTabButton.addActionListener { showDescriptionTab("edit") }
        previewTabButton.addActionListener { showDescriptionTab("preview") }
        regenerateButton.addActionListener { generateDescription() }

        transitionCheckbox.addActionListener {
            transitionCombo.isEnabled = transitionCheckbox.isSelected
        }

        // Default target is already resolved per-repo in prefetch (currentRepo.defaultTarget).
        // The field is initialized there; no async resolution needed here.

        // Description: auto-generate on open for Build tab; empty-state for PR tab.
        if (autoGenerateDescription) {
            generateDescription()
        } else {
            showDescriptionEmpty()
        }
    }

    override fun dispose() {
        descriptionGenJob?.cancel()
        titleGenJob?.cancel()
        userSearchJob?.cancel()
        super.dispose()
    }

    /**
     * Post a runnable to the EDT at this dialog's modality level.
     *
     * Critical: the IntelliJ `invokeLater` default from a background thread resolves to
     * `ModalityState.NON_MODAL`, which is suspended while any modal dialog is open —
     * including this dialog. Any background-thread → EDT hop inside a modal coroutine
     * (LLM title/description generation, user/branch search, etc.) MUST use this helper,
     * otherwise the runnable is queued but never dispatched and the UI spins forever.
     *
     * `stateForComponent(contentPane)` is evaluated lazily so construction-time callers
     * (before createCenterPanel builds the content pane) also work — we fall back to
     * `ModalityState.any()` until the pane is realized.
     */
    private fun invokeLater(runnable: Runnable) {
        val cp = this.contentPane
        val modality = if (cp != null) ModalityState.stateForComponent(cp) else ModalityState.any()
        platformInvokeLater(modality) { runnable.run() }
    }

    override fun createCenterPanel(): JComponent {
        val panel = JPanel(BorderLayout(0, JBUI.scale(8)))
        panel.preferredSize = Dimension(JBUI.scale(620), JBUI.scale(580))
        panel.border = JBUI.Borders.empty(12)

        val content = JPanel().apply {
            layout = BoxLayout(this, BoxLayout.Y_AXIS)
        }

        // Module slot (above Source/Target)
        val branchPanel = JPanel(GridBagLayout())
        val gbc = GridBagConstraints().apply { fill = GridBagConstraints.HORIZONTAL; insets = JBUI.insets(3, 0) }

        var nextRow = 0

        if (context.repos.size == 1) {
            val singleRepo = context.repos[0]
            val labelText = buildRepoLabel(singleRepo)
            gbc.gridx = 0; gbc.gridy = nextRow; gbc.weightx = 0.0
            branchPanel.add(JBLabel("Module:").apply { border = JBUI.Borders.emptyRight(8) }, gbc)
            gbc.gridx = 1; gbc.weightx = 1.0
            // Truncate visually via tooltip for very long labels so column 1 doesn't grow
            // past the dialog width. Full label is still available on hover.
            branchPanel.add(JBLabel(labelText).apply {
                toolTipText = labelText
                maximumSize = Dimension(JBUI.scale(480), preferredSize.height)
            }, gbc)
            nextRow++
        } else if (moduleCombo != null) {
            gbc.gridx = 0; gbc.gridy = nextRow; gbc.weightx = 0.0
            branchPanel.add(JBLabel("Module:").apply { border = JBUI.Borders.emptyRight(8) }, gbc)
            gbc.gridx = 1; gbc.weightx = 1.0
            branchPanel.add(moduleCombo, gbc)
            nextRow++
        }

        gbc.gridx = 0; gbc.gridy = nextRow; gbc.weightx = 0.0
        branchPanel.add(JBLabel("Source:").apply { border = JBUI.Borders.emptyRight(8) }, gbc)
        gbc.gridx = 1; gbc.weightx = 1.0
        branchPanel.add(sourceBranchLabel, gbc)
