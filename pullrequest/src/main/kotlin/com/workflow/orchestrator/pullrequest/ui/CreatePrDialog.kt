    // Both are typed as JBTextField so the rest of the dialog code is uniform.
    private val aiTitleEnabled = settings.state.enableAiTitleGeneration
    private val titleField: JBTextField = if (aiTitleEnabled) {
        ExtendableTextField(40).apply { text = context.defaultTitle }
