            add(ticketChipInput, BorderLayout.CENTER)
        }
        content.add(ticketBorder)
        content.add(Box.createVerticalStrut(JBUI.scale(8)))
        content.add(JSeparator())
        content.add(Box.createVerticalStrut(JBUI.scale(8)))

