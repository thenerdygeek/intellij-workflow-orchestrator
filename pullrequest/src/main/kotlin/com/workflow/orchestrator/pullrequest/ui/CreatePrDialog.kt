/**
 * Modal dialog for creating a pull request on Bitbucket.
 *
 * Phase 5 update — lives in :pullrequest so it can be launched via the
 * [com.workflow.orchestrator.core.bitbucket.CreatePrLauncher] EP from any feature module.
 *
 * Phase 9 update — adds multi-module (multi-Git-root) support:
 *  - A module slot at the top: static JBLabel for single repo, ComboBox for multiple.
 *  - Changing the selected module updates source label, target default, remote branches list.
 *  - PR submit uses the selected repo's projectKey/repoSlug via BitbucketBranchClient.forRepo.
 *
 * Features:
 *  - Searchable target branch
 *  - Multi-ticket input via [TicketChipInput] (max 5 chips, primary drives title + transition)
 *  - AI-generated markdown description with Edit/Preview tabs and provenance footer
 *  - Optional inline AI title generation — gated by `PluginSettings.enableAiTitleGeneration`
 *  - Reviewer autocomplete from Bitbucket users API
 *  - Optional Jira ticket transition
 *  - `autoGenerateDescription` flag — Build-tab callers keep the legacy auto-generate on open;
 *    future PR-tab callers (Phase 6) pass `false` for a manual-generate UX
 */
