private data class RepoComboItem(val repoPrefetch: RepoPrefetch) {
    override fun toString(): String {
        val key = repoPrefetch.config.bitbucketProjectKey.orEmpty()
        val slug = repoPrefetch.config.bitbucketRepoSlug.orEmpty()
        val name = repoPrefetch.config.name.orEmpty().ifBlank { slug }
        return if (key.isNotBlank() && slug.isNotBlank()) "$name ($key/$slug)" else name
    }
