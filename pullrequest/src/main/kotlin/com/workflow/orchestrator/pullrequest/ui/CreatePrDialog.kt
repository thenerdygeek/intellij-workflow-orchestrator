    // --- Module selection ---

    private fun onModuleChanged(newRepo: RepoPrefetch) {
        currentRepo = newRepo
        // Update source label
        sourceBranchLabel.text = newRepo.sourceBranch
        // Reset target to new repo's default. The branch chooser popup is ephemeral
        // (rebuilt per open from currentRepo.remoteBranches) so no cleanup is needed.
        targetField.text = newRepo.defaultTarget
        // Swap repo-level default reviewer chips for the new repo. Manually-added chips
        // (not in repoDefaultReviewerChips) and settings-based chips are preserved.
        val existingRepoDefaults = repoDefaultReviewerChips.toMap()
        existingRepoDefaults.forEach { (username, chip) ->
            selectedReviewers.remove(username)
            reviewerDisplayNames.remove(username)
            reviewerChipsPanel.remove(chip)
        }
        repoDefaultReviewerChips.clear()
        newRepo.repoDefaultReviewers.forEach { user ->
            if (user.name !in selectedReviewers) {
                addReviewerChip(
                    user.name,
                    displayName = user.displayName.ifBlank { null },
                    fromRepoDefaults = true
                )
            }
        }
        reviewerChipsPanel.revalidate()
        reviewerChipsPanel.repaint()
        // Invalidate description — increment the generation counter so any in-flight
        // invokeLater writes from the cancelled job can detect they are stale and discard.
        descriptionGenerationId++
        descriptionGenJob?.cancel()
        titleGenJob?.cancel()
        provenanceLabel.text = ""
        lastGen = null
        showDescriptionEmpty()
    }

    private fun buildRepoLabel(repo: RepoPrefetch): String {
        val key = repo.config.bitbucketProjectKey.orEmpty()
        val slug = repo.config.bitbucketRepoSlug.orEmpty()
        val name = repo.config.name.orEmpty().ifBlank { slug }
        return if (key.isNotBlank() && slug.isNotBlank()) "$name ($key/$slug)" else name
    }

    // --- Branch search ---

    /**
     * Open a branch-chooser popup for the currently selected module. Custom layout: a
     * visible filter text box at the top and a scrollable branch list underneath. The
     * user can only commit a branch by selecting it from the list — manual typing in the
     * target field is disabled. Typing in the filter box filters the list in real time;
     * Enter / double-click / single click on an item commits it and closes the popup;
     * Escape or click-away closes without changing anything.
     */
    private fun showBranchChooser() {
        val allBranches = currentRepo.remoteBranches
        if (allBranches.isEmpty() || !targetField.isShowing) return

        val listModel = javax.swing.DefaultListModel<String>().apply {
            allBranches.forEach { addElement(it) }
        }
        val list = com.intellij.ui.components.JBList(listModel).apply {
            selectionMode = javax.swing.ListSelectionModel.SINGLE_SELECTION
            visibleRowCount = 12
            val currentIdx = allBranches.indexOf(targetField.text)
            if (currentIdx >= 0) selectedIndex = currentIdx
        }
        val scroll = com.intellij.ui.components.JBScrollPane(list).apply {
            border = JBUI.Borders.empty()
            preferredSize = Dimension(JBUI.scale(320), JBUI.scale(260))
        }

        val filterField = com.intellij.ui.SearchTextField().apply {
            textEditor.emptyText.text = "Filter branches"
            border = JBUI.Borders.empty(4, 4, 2, 4)
        }

        fun applyFilter(query: String) {
            val q = query.trim().lowercase()
            listModel.clear()
            allBranches.forEach { branch ->
                if (q.isEmpty() || branch.lowercase().contains(q)) listModel.addElement(branch)
            }
            if (listModel.size() > 0) list.selectedIndex = 0
        }

        filterField.addDocumentListener(object : DocumentListener {
            override fun insertUpdate(e: DocumentEvent) = applyFilter(filterField.text)
            override fun removeUpdate(e: DocumentEvent) = applyFilter(filterField.text)
            override fun changedUpdate(e: DocumentEvent) = applyFilter(filterField.text)
        })

        val content = JPanel(BorderLayout()).apply {
            add(filterField, BorderLayout.NORTH)
            add(scroll, BorderLayout.CENTER)
            border = JBUI.Borders.empty()
        }

        val popup = com.intellij.openapi.ui.popup.JBPopupFactory.getInstance()
            .createComponentPopupBuilder(content, filterField.textEditor)
            .setTitle("Select target branch")
            .setRequestFocus(true)
            .setFocusable(true)
            .setMovable(false)
            .setResizable(true)
            .setCancelOnClickOutside(true)
            .setMinSize(Dimension(JBUI.scale(320), JBUI.scale(260)))
            .createPopup()

        fun commitSelection() {
            val chosen = list.selectedValue ?: return
            targetField.text = chosen
            popup.closeOk(null)
        }

        // Enter in the filter field commits the currently-highlighted list item.
        filterField.textEditor.addKeyListener(object : java.awt.event.KeyAdapter() {
            override fun keyPressed(e: java.awt.event.KeyEvent) {
                when (e.keyCode) {
                    java.awt.event.KeyEvent.VK_ENTER -> { commitSelection(); e.consume() }
                    java.awt.event.KeyEvent.VK_DOWN -> {
                        val next = (list.selectedIndex + 1).coerceAtMost(listModel.size() - 1)
                        if (next >= 0) list.selectedIndex = next
                        e.consume()
                    }
                    java.awt.event.KeyEvent.VK_UP -> {
                        val prev = (list.selectedIndex - 1).coerceAtLeast(0)
                        list.selectedIndex = prev
                        e.consume()
                    }
                }
            }
        })

        // Click / double-click / Enter on the list commits the selection.
        list.addMouseListener(object : java.awt.event.MouseAdapter() {
            override fun mouseClicked(e: java.awt.event.MouseEvent) {
                if (e.clickCount >= 1) commitSelection()
            }
        })

        popup.showUnderneathOf(targetField)
    }

    // --- Reviewer autocomplete ---

    private fun searchUsers() {
        val text = reviewerInput.text.trim()
        if (text.length < 2) {
            reviewerPopup.isVisible = false
            return
        }
        userSearchJob?.cancel()
        // Use the currently-selected repo's Bitbucket config so multi-repo projects query the
        // right Bitbucket instance (fromConfiguredSettings() always picks the primary repo).
        val repoAtSearch = currentRepo
        userSearchJob = scope.launch {
            delay(300)
            val client = BitbucketBranchClient.forRepo(repoAtSearch.config)
                ?: BitbucketBranchClient.fromConfiguredSettings()
                ?: run {
                    log.warn("[Pr:CreateDialog] No Bitbucket client for reviewer search (repo='${repoAtSearch.config.displayLabel}')")
                    return@launch
                }
            // Scope the search to users with REPO_READ on the target repo — matches the
            // Bitbucket web UI behaviour, honours group-expanded permissions, and is
            // callable by ordinary (non-admin) users. The server-side `filter` parameter
            // already matches across username + displayName + emailAddress, so typing a
            // person's real name works out of the box.
            val projectKey = repoAtSearch.config.bitbucketProjectKey?.takeIf { it.isNotBlank() }
            val repoSlug = repoAtSearch.config.bitbucketRepoSlug?.takeIf { it.isNotBlank() }
            when (val result = client.getUsers(text, projectKey, repoSlug)) {
                is ApiResult.Success -> invokeLater {
                    // Show all matches — already-selected users will simply be filtered from
                    // the dropdown items (they stay selectable only once). If the entire
                    // result is already-selected we still want the popup to hide cleanly.
                    showUserResults(result.data.filter { it.name !in selectedReviewers })
                }
                is ApiResult.Error -> {
                    log.warn("[Pr:CreateDialog] User search failed: ${result.message}")
                }
            }
        }
    }

    private fun showUserResults(users: List<BitbucketUser>) {
        reviewerPopup.removeAll()
        if (users.isEmpty()) {
            reviewerPopup.isVisible = false
            return
        }
        // Async guard: if the dialog was closed between the coroutine's invokeLater
        // submission and EDT dispatch, reviewerInput may no longer be on screen.
        // JPopupMenu.show() resolves the invoker's screen location and throws
        // IllegalComponentStateException when the component isn't showing.
        if (!reviewerInput.isShowing) {
            reviewerPopup.isVisible = false
            return
        }
        users.forEach { user ->
            // Primary label = display name (friendly). Secondary = username + email so the
            // reviewer's identity is unambiguous when multiple people share a display name.
            val primary = user.displayName.ifBlank { user.name }
            val secondary = buildString {
                append(user.name)
                if (!user.emailAddress.isNullOrBlank()) append(" · ").append(user.emailAddress)
            }
            val item = JMenuItem("<html><b>$primary</b> &nbsp;<span style='color:#888'>$secondary</span></html>")
            item.addActionListener {
                addReviewerChip(user.name, displayName = user.displayName.ifBlank { null })
                reviewerInput.text = ""
                reviewerPopup.isVisible = false
            }
            reviewerPopup.add(item)
        }
        // pack() so the popup sizes to the new items — without this, Swing can paint the
        // popup at the previous (possibly empty) size and the items render invisibly.
        reviewerPopup.pack()
        reviewerPopup.show(reviewerInput, 0, reviewerInput.height)
    }

    private fun addReviewerChip(
        username: String,
        displayName: String? = null,
        fromRepoDefaults: Boolean = false
    ) {
        if (username in selectedReviewers) return
        selectedReviewers.add(username)
        // Remember the display name for subsequent lookups (e.g. if we ever need to re-render).
        if (!displayName.isNullOrBlank()) reviewerDisplayNames[username] = displayName
        // Chip label = display name if known, else username. Tooltip always shows the raw
        // username so the user can verify the identity that will go to the Bitbucket API.
        val chipLabel = displayName?.takeIf { it.isNotBlank() } ?: username
        val chipTooltip = if (!displayName.isNullOrBlank() && displayName != username) {
            "@$username"
        } else {
            username
        }
        val chip = JPanel(FlowLayout(FlowLayout.LEFT, JBUI.scale(4), 0)).apply {
            background = StatusColors.CARD_BG
            border = JBUI.Borders.empty(1, 6)
            toolTipText = chipTooltip
            add(JBLabel(chipLabel).apply {
                font = font.deriveFont(11f)
                toolTipText = chipTooltip
            })
            val removeBtn = JBLabel("✕").apply {
                cursor = Cursor.getPredefinedCursor(Cursor.HAND_CURSOR)
                foreground = StatusColors.SECONDARY_TEXT
            }
            removeBtn.addMouseListener(object : java.awt.event.MouseAdapter() {
                override fun mouseClicked(e: java.awt.event.MouseEvent?) {
                    selectedReviewers.remove(username)
                    reviewerDisplayNames.remove(username)
                    repoDefaultReviewerChips.remove(username)
                    reviewerChipsPanel.remove(this@apply)
                    reviewerChipsPanel.revalidate()
                    reviewerChipsPanel.repaint()
                }
            })
            add(removeBtn)
        }
        if (fromRepoDefaults) repoDefaultReviewerChips[username] = chip
        reviewerChipsPanel.remove(reviewerInput)
        reviewerChipsPanel.add(chip)
        reviewerChipsPanel.add(reviewerInput)
        reviewerChipsPanel.revalidate()
    }

